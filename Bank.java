// class Bank
public class Bank {

    // int total private, with getter and setter
    private int total;

    // constructor with total as parameter
    public Bank(int total) {
        this.total = total;
    }

    // getter and setter for total
    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
